-- Database Schema Creation
DROP DATABASE IF EXISTS ssafy_final_db;
CREATE DATABASE IF NOT EXISTS ssafy_final_db;
USE ssafy_final_db;

-- User Table
CREATE TABLE User (
    user_id VARCHAR(10) COLLATE utf8mb4_bin NOT NULL PRIMARY KEY,
    nickname VARCHAR(50) NOT NULL,
    password VARCHAR(255) NOT NULL,
    email VARCHAR(100) NOT NULL,
    user_img VARCHAR(50)
);

-- Sport_Team Table
CREATE TABLE Sport_Team (
    team_id INT NOT NULL PRIMARY KEY,
    sport_name VARCHAR(50) NOT NULL,
    team_name VARCHAR(50) NOT NULL,
    team_logo VARCHAR(255) NOT NULL
);

-- Favorite_Team Table
CREATE TABLE Favorite_Team (
    user_id VARCHAR(10) COLLATE utf8mb4_bin NOT NULL,
    team_id INT NOT NULL,
    PRIMARY KEY (user_id, team_id),
    FOREIGN KEY (user_id) REFERENCES User(user_id) ON DELETE CASCADE,
    FOREIGN KEY (team_id) REFERENCES Sport_Team(team_id) ON DELETE CASCADE
);

-- Match_Schedule Table
CREATE TABLE Match_Schedule (
    match_id INT NOT NULL PRIMARY KEY,
    date DATE,
    time TIME,
    hometeam_id INT,
    awayteam_id INT,
    location VARCHAR(50),
    FOREIGN KEY (hometeam_id) REFERENCES Sport_Team(team_id) ON DELETE CASCADE,
    FOREIGN KEY (awayteam_id) REFERENCES Sport_Team(team_id) ON DELETE CASCADE
);
